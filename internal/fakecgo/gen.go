type Arg struct {
	Name string
	Type string
}

type Symbol struct {
	Name   string
	Args   [5]Arg
	Return string
	Goos   []string // empty means all GOOSes
}

type SharedObject struct {
	Name    string
	Symbols []Symbol
}

type LocatedSymbols struct {
	Tags          string
	Symbols       []Symbol
	SharedObjects []SharedObject
}

