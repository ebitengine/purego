var funcs = map[string]any{
	"hasPrefix": strings.HasPrefix,
	"imports":   imports,
}

func imports(symbols []Symbol) string {
	if len(symbols) == 0 {
		return ""
	}
	imports := make(map[string]struct{})
	imports["unsafe"] = struct{}{}
	for _, s := range symbols {
		for _, arg := range s.Args {
			if pkg, _, found := strings.Cut(arg.Type, "."); found {
				pkg = strings.TrimPrefix(pkg, "*")
				imports[pkg] = struct{}{}
			}
		}
	}
	if len(imports) == 1 {
		return `import "unsafe"`
	}
	result := make([]string, 0, len(imports))
	for imp := range imports {
		result = append(result, imp)
	}
	sort.Strings(result)
	var sb strings.Builder
	sb.WriteString("import (")
	for _, imp := range result {
		sb.WriteString("\n\t\"")
		sb.WriteString(imp)
		sb.WriteString("\"")
	}
	sb.WriteString("\n)")
	return sb.String()
}

// filterSymbols filters the symbols by GOOS.
// If goos is empty, it returns symbols that are not specific to any GOOS.
func filterSymbols(symbols []Symbol, goos string) []Symbol {
	filtered := make([]Symbol, 0, len(symbols))
	for _, s := range symbols {
		if (s.Goos == nil && goos == "") || contains(s.Goos, goos) {
			filtered = append(filtered, s)
		}
	}
	return filtered
}

func contains(slice []string, item string) bool {
	for _, s := range slice {
		if s == item {
			return true
		}
	}
	return false
}

var allOSes = []string{"darwin", "freebsd", "linux", "netbsd"}

func run() error {
	t, err := template.New("zsymbol.go").Funcs(funcs).Parse(templateSymbols)
	if err != nil {
		return err
	}
	f, err := os.Create("zsymbols.go")
	defer f.Close()
	if err != nil {
		return err
	}
	allSymbols := append(append([]Symbol{}, libcSymbols...), pthreadSymbols...)
	goosAgnosticSymbols := LocatedSymbols{
		Symbols: filterSymbols(allSymbols, ""),
		Tags:    "(" + strings.Join(allOSes, " || ") + ")",
	}
	buf := new(bytes.Buffer)
	if err := t.Execute(buf, goosAgnosticSymbols); err != nil {
		return err
	}
	source, err := format.Source(buf.Bytes())
	if err != nil {
		return err
	}
	if _, err = f.Write(source); err != nil {
		return err
	}
	t, err = template.New("ztrampolines_stubs.s").Funcs(funcs).Parse(templateTrampolinesStubs)
	if err != nil {
		return err
	}
	f, err = os.Create("ztrampolines_stubs.s")
	defer f.Close()
	if err != nil {
		return err
	}
	if err := t.Execute(f, goosAgnosticSymbols); err != nil {
		return err
	}
	t, err = template.New("zsymbols_goos.go").Funcs(funcs).Parse(templateSymbols)
	if err != nil {
		return err
	}
	for _, goos := range allOSes {
		goosSymbols := filterSymbols(allSymbols, goos)
		f, err = os.Create(fmt.Sprintf("zsymbols_%s.go", goos))
		defer f.Close()
		if err != nil {
			return err
		}
		b := &bytes.Buffer{}
		var libcSO, pthreadSO string
		switch goos {
		case "darwin":
			libcSO = "/usr/lib/libSystem.B.dylib"
			pthreadSO = "/usr/lib/libSystem.B.dylib"
		case "freebsd":
			libcSO = "libc.so.7"
			pthreadSO = "libpthread.so"
		case "linux":
			libcSO = "libc.so.6"
			pthreadSO = "libpthread.so.0"
		case "netbsd":
			libcSO = "libc.so"
			pthreadSO = "libpthread.so"
		default:
			return fmt.Errorf("unsupported OS: %s", goos)
		}
		located := LocatedSymbols{
			SharedObjects: []SharedObject{
				{libcSO, append(filterSymbols(libcSymbols, ""), filterSymbols(libcSymbols, goos)...)},
				{pthreadSO, append(filterSymbols(pthreadSymbols, ""), filterSymbols(pthreadSymbols, goos)...)},
			},
			Symbols: goosSymbols,
			Tags:    goos,
		}
		if err = t.Execute(b, located); err != nil {
			return err
		}
		var src []byte
		src, err = format.Source(b.Bytes())
		if err != nil {
			return err
		}
		if _, err = f.Write(src); err != nil {
			return err
		}
		if len(goosSymbols) == 0 {
			continue
		}
		t, err := template.New("ztrampolines_goos.s").Funcs(funcs).Parse(templateTrampolinesStubs)
		if err != nil {
			return err
		}
		f, err = os.Create(fmt.Sprintf("ztrampolines_%s.s", goos))
		if err != nil {
			return err
		}
		if err := t.Execute(f, LocatedSymbols{Tags: goos, Symbols: goosSymbols}); err != nil {
			f.Close()
			return err
		}
		if err := f.Close(); err != nil {
			return err
		}
	}
	return nil
}

func main() {
	if err := run(); err != nil {
		log.Fatal(err)
	}
}
