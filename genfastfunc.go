func emitTrailingFloat1Closures(buf *bytes.Buffer, cfg fastPathConfig, p fastPattern) {
	maxTrailingInts := max(p.MaxInts-1, 0)
	typeName := "float32"
	bitsFunc := "math.Float32bits"
	suffix := "Float32x1"
	if p.FloatBits == 64 {
		typeName = "float64"
		bitsFunc = "math.Float64bits"
		suffix = "Float64x1"
	}

	var cases []trailingFloat1CaseData
	for n := p.MinInts; n <= maxTrailingInts; n++ {
		intParams := ""
		if n > 0 {
			intParams = argList(n) + " uintptr, "
		}
		var intsInit string
		if n == 0 {
			intsInit = fmt.Sprintf("var ints [%d]uintptr", cfg.MaxIntArgs)
		} else {
			intsInit = fmt.Sprintf("ints := [%d]uintptr{%s}", cfg.MaxIntArgs, argList(n))
		}
		cases = append(cases, trailingFloat1CaseData{
			N: n, IntParams: intParams, TypeName: typeName,
			IntsInit: intsInit, BitsFunc: bitsFunc,
		})
	}
	execTemplate(buf, "trailingFloat1_"+suffix, tmplTrailingFloat1, trailingFloat1Data{
		Suffix: suffix, TypeName: typeName, Cases: cases,
	})
}

