func emitFastCallN(buf *bytes.Buffer, cfg fastPathConfig) {
	var data []fastCallData
	for n := 0; n <= cfg.MaxIntArgs; n++ {
		params := "cfn"
		if n > 0 {
			params += ", " + argList(n)
		}
		data = append(data, fastCallData{
			N:      n,
			Params: params,
			A1to8:  buildA1to8(n),
			A9to15: buildA9to15(n),
		})
	}
	execTemplate(buf, "fastCallN", tmplFastCallN, data)
}

func buildA1to8(n int) string {
	if n == 0 {
		return "0, 0, 0, 0, 0, 0, 0, 0"
	}
	parts := make([]string, 8)
	for i := range 8 {
		if i < n {
			parts[i] = fmt.Sprintf("a%d", i+1)
		} else {
			parts[i] = "0"
		}
	}
	return strings.Join(parts, ", ")
}

func buildA9to15(n int) string {
	fields := []string{"s.a9", "s.a10", "s.a11", "s.a12", "s.a13", "s.a14", "s.a15"}
	if n <= 8 {
		return "\ts.a9, s.a10, s.a11, s.a12, s.a13, s.a14, s.a15 = 0, 0, 0, 0, 0, 0, 0\n"
	}
	overflow := make([]string, n-8)
	for i := 8; i < n; i++ {
		overflow[i-8] = fmt.Sprintf("a%d", i+1)
	}
	s := fmt.Sprintf("\t%s = %s\n", strings.Join(fields[:len(overflow)], ", "), strings.Join(overflow, ", "))
	remaining := fields[len(overflow):]
	if len(remaining) > 0 {
		zeros := make([]string, len(remaining))
		for i := range zeros {
			zeros[i] = "0"
		}
		s += fmt.Sprintf("\t%s = %s\n", strings.Join(remaining, ", "), strings.Join(zeros, ", "))
	}
	return s
}

func emitFastCallIF(buf *bytes.Buffer, cfg fastPathConfig) {
	type ifCaseData struct {
		N        int
		Assign   string // "s.a9, s.a10 = ints[8], ints[9]"
		ZeroRest string // "s.a11, ... = 0, 0, ..."
		HasZeros bool
	}

	type ifData struct {
		MaxIntArgs int
		Cases      []ifCaseData
	}

	var cases []ifCaseData
	for ni := 10; ni <= cfg.MaxIntArgs; ni++ {
		fields := []string{"s.a9", "s.a10", "s.a11", "s.a12", "s.a13", "s.a14", "s.a15"}
		parts := make([]string, ni-8)
		for i := 8; i < ni; i++ {
			parts[i-8] = fmt.Sprintf("ints[%d]", i)
		}
		usedFields := fields[:ni-8]
		remaining := fields[ni-8:]
		c := ifCaseData{
			N:      ni,
			Assign: strings.Join(usedFields, ", ") + " = " + strings.Join(parts, ", "),
		}
		if len(remaining) > 0 {
			zeros := make([]string, len(remaining))
			for i := range zeros {
				zeros[i] = "0"
			}
			c.ZeroRest = strings.Join(remaining, ", ") + " = " + strings.Join(zeros, ", ")
			c.HasZeros = true
		}
		cases = append(cases, c)
	}

	execTemplate(buf, "fastCallIF", tmplFastCallIF, ifData{
		MaxIntArgs: cfg.MaxIntArgs,
		Cases:      cases,
	})
}

