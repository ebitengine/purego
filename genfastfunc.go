func emitTrailingFloatNClosures(buf *bytes.Buffer, cfg fastPathConfig, p fastPattern) {
	maxTrailingInts := max(p.MaxInts-p.NumFloats, 0)
	typeName := "float32"
	bitsFunc := "math.Float32bits"
	if p.FloatBits == 64 {
		typeName = "float64"
		bitsFunc = "math.Float64bits"
	}
	funcName := "registerFastFunc" + p.Name

	var cases []trailingFloatNCaseData
	for n := p.MinInts; n <= maxTrailingInts; n++ {
		intParams := ""
		if n > 0 {
			intParams = argList(n) + " uintptr, "
		}
		var intsInit string
		if n == 0 {
			intsInit = fmt.Sprintf("var ints [%d]uintptr", cfg.MaxIntArgs)
		} else {
			intsInit = fmt.Sprintf("ints := [%d]uintptr{%s}", cfg.MaxIntArgs, argList(n))
		}
		var floatAssigns string
		for fi := 0; fi < p.NumFloats; fi++ {
			floatAssigns += fmt.Sprintf("\t\t\t\tfloats[%d] = uintptr(%s(f%d))\n", fi, bitsFunc, fi+1)
		}
		cases = append(cases, trailingFloatNCaseData{
			N: n, IntParams: intParams,
			FloatParams:  floatParamList(p.NumFloats, p.FloatBits),
			IntsInit:     intsInit,
			FloatAssigns: floatAssigns,
			NumFloats:    p.NumFloats,
		})
	}
	execTemplate(buf, "trailingFloatN_"+p.Name, tmplTrailingFloatN, trailingFloatNData{
		FuncName: funcName, NumFloats: p.NumFloats, TypeName: typeName, Cases: cases,
	})
}

